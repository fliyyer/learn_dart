void main() {
  var value1 = 10;
  var value2 = 20;

  print(value1 == value2); // sama dengan
  print(value1 != value2); // tidak sama dengan
  print(value1 > value2); // lebih besar
  print(value1 < value2); // lebih kecil
  print(value1 >= value2); // lebih besar sama dengan
  print(value1 <= value2); // lebih kecil sama dengan
}
