void main() {
  var number = 100;

  switch (number) {
    case 1:
      print("Satu");
      break;
    case 2:
      print("Dua");
      break;
    case 3:
      print("Tiga");
      break;
    case 4:
      print("Empat");
      break;
    default:
      print("Tidak ada yang cocok");
      break;
  }
}
